    const form = document.getElementById('contactForm');
    const formMessage = document.getElementById('formMessage');
    const btn = document.getElementById("submit");

    btn.disabled = true; 

    const nameInput = document.getElementById("name");
    const emailInput = document.getElementById("email");
    const messageInput = document.getElementById("message");

  
    function nameValidation() {
      const name = nameInput.value.trim();
      if (name.length < 3 || name.length > 10) {
        formMessage.innerHTML = "Your name is not valid <br> Name must be between 3 and 10 characters.";
        formMessage.style.color = "red";
        return false;
      }
      else {
      return true;
      formMessage.innerText="";
      }
    }


    function emailValidation() {
      const email = emailInput.value.trim();
      if (email === "" || !email.includes("@") || !email.includes(".")) {
        formMessage.innerHTML = "Email is not valid";
        formMessage.style.color = "red";
        return false;
      }
      else {
      return true;
      formMessage.innerText="";
      }
    }


    function messageValidation() {
      const message = messageInput.value.trim();
      if (message.length < 10) {
        formMessage.innerHTML = "Your message is not valid <br> Message must be more than 10 characters.";
        formMessage.style.color = "red";
        return false;
      }
      else {
      return true;
      formMessage.innerText="";
      }
    }

    function validateForm() {
      if (nameValidation() && emailValidation() && messageValidation()) {
        btn.disabled = false;
      } else {
        btn.disabled = true;
      }
    };

    nameInput.addEventListener("input", validateForm);
    emailInput.addEventListener("input", validateForm);
    messageInput.addEventListener("input", validateForm);

    form.addEventListener("submit", (event) => {
      event.preventDefault();

      if (nameValidation() && emailValidation() && messageValidation()) {
        const date = new Date();
        formMessage.innerHTML = `Thank you for your message! <br> Sent on: ` + date.toDateString();
        formMessage.style.color = "green";
        form.reset();
        btn.disabled = true;
      }
    });


    

  </script>

