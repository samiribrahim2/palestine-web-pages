      <h2>Developments of the Palestinian Issue</h2>
      <article>
        <h3>The 1948 Nakba</h3>
        <p>In 1948, the State of Israel was declared following UN Resolution 181, which called for the partition
          of Palestine into two states: Arab and Jewish. Palestinians and Arabs rejected the resolution, and
          the 1948 War broke out, leading to the displacement of hundreds of thousands of Palestinians from
          their lands in what is known as the Nakba (catastrophe).</p>
      </article>
      <article>
        <h3>The 1967 Naksa and Occupation</h3>
        <p>During the 1967 War, Israel occupied the West Bank, Gaza Strip, and East Jerusalem, exacerbating the
          suffering of the Palestinian people and worsening the crisis. These occupied areas became central to
          the Palestinian demand for independence and statehood.</p>
      </article>
      <article>
        <h3>Peace Agreements</h3>
        <p>The Palestinian issue witnessed numerous attempts at peaceful settlement, starting with the Madrid
          Conference (1991) and followed by the Oslo Accords (1993), which provided for the establishment of a
          temporary Palestinian Authority and negotiations on final status issues. However, these agreements
          failed to meet Palestinian aspirations for full independence.</p>
      </article>
    </section>
